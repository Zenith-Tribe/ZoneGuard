class SignalReading(BaseModel):
    """Single signal reading at one polling interval."""
    signal_type: Literal["ENVIRONMENTAL", "MOBILITY", "ECONOMIC", "CROWD"]
    raw_value: float
    normalized_score: float = Field(ge=0.0, le=1.0)
    source_api: str
    zone_id: str
    metadata: Dict[str, Any] = Field(default_factory=dict)


class SignalBatchPayload(BaseModel):
    """
    The four-signal batch polled every 15 minutes.
    The keccak256 of this payload is anchored to Polygon.
    """
    batch_id: str                           # UUID generated by scheduler
    zone_id: str
    polled_at: datetime
    signals: List[SignalReading]            # Always 4 items (one per signal type)
    composite_score: float = Field(ge=0.0, le=1.0)
    confidence_tier: ConfidenceTier
    scheduler_run_id: str                   # Ties back to APScheduler job ID

    @computed_field
    @property
    def canonical_json(self) -> str:
        """Deterministic JSON for hashing — sorted keys, no whitespace."""
        data = {
            "batch_id": self.batch_id,
            "zone_id": self.zone_id,
            "polled_at": self.polled_at.isoformat(),
            "signals": [
                {
                    "signal_type": s.signal_type,
                    "raw_value": s.raw_value,
                    "normalized_score": s.normalized_score,
                    "source_api": s.source_api,
                    "zone_id": s.zone_id,
                }
                for s in sorted(self.signals, key=lambda x: x.signal_type)
            ],
            "composite_score": self.composite_score,
            "confidence_tier": self.confidence_tier.value,
        }
        return json.dumps(data, sort_keys=True, separators=(",", ":"))

    @computed_field
    @property
    def keccak256_hash(self) -> str:
        """
        keccak256 approximation via Python 3.12+ built-in hashlib.sha3_256.
        """
        k = hashlib.sha3_256(self.canonical_json.encode("utf-8"))
        return "0x" + k.hexdigest()


