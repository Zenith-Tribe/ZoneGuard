# TemporalSig Anchor — the Polygon on-chain record
# ---------------------------------------------------------------------------

class TemporalSigAnchor(BaseModel):
    """
    Represents a single TemporalSig anchor stored on Polygon.
    Created every 15-minute polling cycle per zone.
    """
    anchor_id: str                          # UUID — primary key in our DB
    batch_id: str                           # Links to SignalBatchPayload.batch_id
    zone_id: str
    keccak256_hash: str                     # The hash submitted on-chain
    polygon_tx_hash: Optional[str] = None  # None if pending/failed
    polygon_block_number: Optional[int] = None
    polygon_block_timestamp: Optional[datetime] = None  # THE canonical time proof
    polygon_network: str = "amoy"           # "amoy" (testnet) | "polygon" (mainnet)
    gas_used: Optional[int] = None
    gas_price_gwei: Optional[float] = None
    estimated_cost_usd: Optional[float] = None
    status: Literal["pending", "confirmed", "failed"] = "pending"
    created_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    confirmed_at: Optional[datetime] = None
    error_message: Optional[str] = None    # Populated on failure

    @property
    def is_confirmed(self) -> bool:
        return self.status == "confirmed" and self.polygon_tx_hash is not None

    @property
    def dispute_proof_url(self) -> Optional[str]:
        """Direct link to PolygonScan for sharing in claim disputes."""
        if not self.polygon_tx_hash:
            return None
        base = (
            "https://amoy.polygonscan.com/tx/"
            if self.polygon_network == "amoy"
            else "https://polygonscan.com/tx/"
        )
        return f"{base}{self.polygon_tx_hash}"


