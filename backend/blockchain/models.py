# ZoneChain Ledger Event Envelope
# ---------------------------------------------------------------------------

class ZoneChainEvent(BaseModel):
    """
    The canonical envelope written as a Fabric transaction.
    All ZoneChain writes use this structure — the chaincode reads `event_type`
    to route to the correct ledger collection.
    """
    event_id: str                           # UUID — also the Fabric transaction key
    event_type: ChainEventType
    written_by: NodeRole = NodeRole.ZONEGUARD
    written_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    schema_version: str = "1.0.0"

    # Exactly one of these will be populated
    claim_payload: Optional[ClaimEventPayload] = None
    policy_payload: Optional[PolicyEventPayload] = None
    payout_payload: Optional[PayoutEventPayload] = None
    parameter_payload: Optional[ParameterChangePayload] = None
    signal_payload: Optional[SignalBatchPayload] = None

    @model_validator(mode="after")
    def validate_exactly_one_payload(self) -> "ZoneChainEvent":
        payloads = [
            self.claim_payload,
            self.policy_payload,
            self.payout_payload,
            self.parameter_payload,
            self.signal_payload,
        ]
        populated = [p for p in payloads if p is not None]
        if len(populated) != 1:
            raise ValueError(
                f"ZoneChainEvent must have exactly one payload, got {len(populated)}"
            )
        return self

    def to_fabric_json(self) -> str:
        """Serialize for Fabric chaincode invoke — compact, deterministic."""
        return self.model_dump_json(exclude_none=False)


